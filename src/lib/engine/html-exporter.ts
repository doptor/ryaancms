// ── Image Collector ─────────────────────────────────────────

function collectImageUrls(config: AppConfig): string[] {
  const urls = new Set<string>();
  const walk = (obj: any) => {
    if (!obj || typeof obj !== "object") return;
    for (const [key, val] of Object.entries(obj)) {
      if (typeof val === "string" && /^https?:\/\/.+\.(png|jpe?g|gif|webp|svg|ico|avif|bmp)/i.test(val)) {
        urls.add(val);
      } else if (typeof val === "object") {
        walk(val);
      }
    }
  };
  walk(config);
  return Array.from(urls);
}

async function downloadImages(urls: string[]): Promise<Map<string, { blob: Blob; localPath: string }>> {
  const map = new Map<string, { blob: Blob; localPath: string }>();
  const tasks = urls.map(async (url, i) => {
    try {
      const res = await fetch(url);
      if (!res.ok) return;
      const blob = await res.blob();
      const ext = url.match(/\.(png|jpe?g|gif|webp|svg|ico|avif|bmp)/i)?.[1] || "png";
      const localPath = `images/img-${i + 1}.${ext}`;
      map.set(url, { blob, localPath });
    } catch { /* skip failed downloads */ }
  });
  await Promise.all(tasks);
  return map;
}

function rewriteUrl(url: string, imageMap: Map<string, { blob: Blob; localPath: string }>): string {
  return imageMap.get(url)?.localPath || url;
}

