// ── Component Router ────────────────────────────────────────

function renderComponent(comp: ComponentConfig, config: AppConfig, imageMap: Map<string, { blob: Blob; localPath: string }>, pages: PageConfig[]): string {
  switch (comp.type) {
    case "navbar": return renderNavbar(comp, config, imageMap, pages);
    case "hero": return renderHero(comp, config);
    case "footer": return renderFooter(comp, config);
    case "stats_row": return renderStatsRow(comp);
    case "features_grid": return renderFeaturesGrid(comp);
    case "pricing_table": return renderPricingTable(comp);
    case "testimonials": return renderTestimonials(comp);
    case "faq": return renderFAQ(comp);
    case "final_cta": return renderFinalCTA(comp);
    case "contact_form": return renderContactForm(comp);
    case "newsletter_cta": return renderNewsletterCTA(comp);
    case "card_grid": return renderCardGrid(comp);
    case "team_section": return renderTeamSection(comp);
    case "blog_preview": return renderBlogPreview(comp);
    default: return renderGenericComponent(comp);
  }
}

