// ============================================================
// Engine 9: Source Code Generator
// Converts AppConfig into real React + Express + Prisma files
// ============================================================

import type { AppConfig, PageConfig, ComponentConfig, CollectionConfig, CollectionField, RoleConfig } from "./component-registry";

export interface GeneratedFile {
  path: string;
  content: string;
  language: string;
}

export interface GeneratedProject {
  files: GeneratedFile[];
  summary: {
    total_files: number;
    frontend_files: number;
    backend_files: number;
    config_files: number;
  };
}

// ─── Main Generator ─────────────────────────────────────────

export function generateProjectCode(config: AppConfig, sql: string): GeneratedProject {
  const files: GeneratedFile[] = [];

  // 1. Config / Root files
  files.push(...generateRootFiles(config));

  // 2. Frontend: React + Tailwind
  files.push(...generateFrontendFiles(config));

  // 3. Backend: Express + Prisma
  files.push(...generateBackendFiles(config));

  // 4. Database
  files.push({ path: "database/schema.sql", content: sql || "-- No schema generated", language: "sql" });
  files.push({ path: "database/prisma/schema.prisma", content: generatePrismaSchema(config), language: "prisma" });

  // 5. Docker & CI
  files.push(...generateInfraFiles(config));

  const frontend = files.filter(f => f.path.startsWith("frontend/")).length;
  const backend = files.filter(f => f.path.startsWith("backend/")).length;

  return {
    files,
    summary: {
      total_files: files.length,
      frontend_files: frontend,
      backend_files: backend,
      config_files: files.length - frontend - backend,
    },
  };
}

