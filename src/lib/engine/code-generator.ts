// ─── Prisma Schema ───────────────────────────────────────────

function generatePrismaSchema(config: AppConfig): string {
  const models: string[] = [];

  // User model (always needed)
  models.push(`model User {
  id        String   @id @default(uuid())
  email     String   @unique
  password  String
  name      String
  role      String   @default("user")
  createdAt DateTime @default(now()) @map("created_at")
  updatedAt DateTime @updatedAt @map("updated_at")

  @@map("users")
}`);

  // Collection models
  for (const col of config.collections) {
    const modelName = pascalCase(col.name);
    const fieldLines: string[] = [
      `  id        String   @id @default(uuid())`,
    ];

    if (col.rls) {
      fieldLines.push(`  userId    String   @map("user_id")`);
      fieldLines.push(`  user      User     @relation(fields: [userId], references: [id], onDelete: Cascade)`);
    }

    for (const field of col.fields) {
      if (["id", "user_id", "created_at", "updated_at"].includes(field.name)) continue;
      const prismaType = fieldTypeToPrisma(field.type);
      const optional = field.required ? "" : "?";
      const attrs: string[] = [];
      if (field.unique) attrs.push("@unique");
      if (field.default) attrs.push(`@default("${field.default}")`);
      fieldLines.push(`  ${camelCase(field.name).replace(/\s+/g, "")}  ${prismaType}${optional} ${attrs.join(" ")} @map("${field.name.toLowerCase().replace(/\s+/g, "_")}")`);
    }

    fieldLines.push(`  createdAt DateTime @default(now()) @map("created_at")`);
    fieldLines.push(`  updatedAt DateTime @updatedAt @map("updated_at")`);
    if (col.soft_delete) {
      fieldLines.push(`  deletedAt DateTime? @map("deleted_at")`);
    }

    models.push(`model ${modelName} {\n${fieldLines.join("\n")}\n\n  @@map("${col.name.toLowerCase().replace(/\s+/g, "_")}")\n}`);
  }

  return `// Prisma Schema — Generated by RyaanCMS AI Builder
// Project: ${config.title}

generator client {
  provider = "prisma-client-js"
}

datasource db {
  provider = "postgresql"
  url      = env("DATABASE_URL")
}

${models.join("\n\n")}
`;
}

