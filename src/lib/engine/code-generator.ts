function generateAppComponent(config: AppConfig): string {
  const imports = config.pages.map(p => `import ${pascalCase(p.name)}Page from './pages/${pascalCase(p.name)}Page';`).join("\n");
  const routes = config.pages.map(p => {
    const route = p.route || `/${p.name.toLowerCase().replace(/\s+/g, "-")}`;
    return `        <Route path="${route}" element={<${pascalCase(p.name)}Page />} />`;
  }).join("\n");

  return `import { Routes, Route } from 'react-router-dom';
${imports}

export default function App() {
  return (
    <div className="min-h-screen bg-gray-50">
      <Routes>
${routes}
        <Route path="*" element={<div className="p-8 text-center"><h1 className="text-2xl font-bold">404 — Page Not Found</h1></div>} />
      </Routes>
    </div>
  );
}
`;
}

function generatePageComponent(page: PageConfig, config: AppConfig): string {
  const componentImports = page.components
    .map(c => pascalCase(c.type))
    .filter((v, i, a) => a.indexOf(v) === i)
    .map(name => `import { ${name} } from '@/components/${name}';`)
    .join("\n");

  const componentJsx = page.components
    .map(c => {
      const propsStr = c.props
        ? Object.entries(c.props).map(([k, v]) => `${k}={${JSON.stringify(v)}}`).join(" ")
        : "";
      return `      <${pascalCase(c.type)} ${propsStr} />`;
    })
    .join("\n");

  const isDashboard = page.layout === "dashboard";

  return `${componentImports}

export default function ${pascalCase(page.name)}Page() {
  return (
    <div className="${isDashboard ? "flex min-h-screen" : ""}">
      ${isDashboard ? '<main className="flex-1 p-6">' : '<main>'}
${componentJsx}
      </main>
    </div>
  );
}
`;
}

