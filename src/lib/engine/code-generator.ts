function generateCrudHook(col: CollectionConfig): string {
  const name = pascalCase(col.name);
  const slug = col.name.toLowerCase().replace(/\s+/g, "-");

  return `import { useState, useEffect, useCallback } from 'react';
import { api } from '@/lib/api';

export function use${name}() {
  const [items, setItems] = useState<any[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  const fetch = useCallback(async () => {
    setLoading(true);
    try {
      const res = await api.get('/${slug}');
      setItems(res.data);
      setError(null);
    } catch (err: any) {
      setError(err.message);
    } finally {
      setLoading(false);
    }
  }, []);

  useEffect(() => { fetch(); }, [fetch]);

  const create = async (data: any) => {
    const res = await api.post('/${slug}', data);
    setItems(prev => [...prev, res.data]);
    return res.data;
  };

  const update = async (id: string, data: any) => {
    const res = await api.put(\`/${slug}/\${id}\`, data);
    setItems(prev => prev.map(item => item.id === id ? res.data : item));
    return res.data;
  };

  const remove = async (id: string) => {
    await api.delete(\`/${slug}/\${id}\`);
    setItems(prev => prev.filter(item => item.id !== id));
  };

  return { items, loading, error, fetch, create, update, remove };
}
`;
}

function generateExpressRoute(col: CollectionConfig, config: AppConfig): string {
  const name = pascalCase(col.name);
  const model = camelCase(col.name);
  const slug = col.name.toLowerCase().replace(/\s+/g, "-");

  return `import { Router } from 'express';
import { PrismaClient } from '@prisma/client';
import { authenticate, authorize, AuthRequest } from '../middleware/auth';

const router = Router();
const prisma = new PrismaClient();

// GET /${slug} — List all
router.get('/', ${col.rls ? "authenticate, " : ""}async (req: AuthRequest, res) => {
  try {
    const items = await prisma.${model}.findMany({
      ${col.rls ? "where: { userId: req.userId }," : ""}
      orderBy: { createdAt: 'desc' },
    });
    res.json(items);
  } catch (err) {
    res.status(500).json({ error: 'Failed to fetch ${col.name}' });
  }
});

// GET /${slug}/:id — Get one
router.get('/:id', ${col.rls ? "authenticate, " : ""}async (req: AuthRequest, res) => {
  try {
    const item = await prisma.${model}.findFirst({
      where: { id: req.params.id${col.rls ? ", userId: req.userId" : ""} },
    });
    if (!item) return res.status(404).json({ error: 'Not found' });
    res.json(item);
  } catch (err) {
    res.status(500).json({ error: 'Failed to fetch ${col.name}' });
  }
});

// POST /${slug} — Create
router.post('/', authenticate, async (req: AuthRequest, res) => {
  try {
    const item = await prisma.${model}.create({
      data: { ...req.body, ${col.rls ? "userId: req.userId!," : ""} },
    });
    res.status(201).json(item);
  } catch (err) {
    res.status(500).json({ error: 'Failed to create ${col.name}' });
  }
});

// PUT /${slug}/:id — Update
router.put('/:id', authenticate, async (req: AuthRequest, res) => {
  try {
    const item = await prisma.${model}.update({
      where: { id: req.params.id },
      data: req.body,
    });
    res.json(item);
  } catch (err) {
    res.status(500).json({ error: 'Failed to update ${col.name}' });
  }
});

// DELETE /${slug}/:id — Delete
router.delete('/:id', authenticate, async (req: AuthRequest, res) => {
  try {
    await prisma.${model}.delete({ where: { id: req.params.id } });
    res.status(204).send();
  } catch (err) {
    res.status(500).json({ error: 'Failed to delete ${col.name}' });
  }
});

export { router as ${camelCase(col.name)}Router };
`;
}

function generateFrontendTypes(config: AppConfig): string {
  const types = config.collections.map(col => {
    const fields = col.fields.map(f => {
      const tsType = fieldTypeToTS(f.type);
      return `  ${camelCase(f.name)}: ${tsType}${f.required ? "" : " | null"};`;
    }).join("\n");

    return `export interface ${pascalCase(col.name)} {
  id: string;
${fields}
  createdAt: string;
  updatedAt: string;
}`;
  }).join("\n\n");

  return `// Auto-generated types from AppConfig\n\n${types}\n`;
}

// ─── Helpers ─────────────────────────────────────────────────

function pascalCase(str: string): string {
  return str
    .replace(/[^a-zA-Z0-9]+/g, " ")
    .split(" ")
    .map(w => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join("");
}

function camelCase(str: string): string {
  const pascal = pascalCase(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

function fieldTypeToTS(type: string): string {
  const map: Record<string, string> = {
    text: "string", number: "number", boolean: "boolean",
    date: "string", timestamp: "string", relation: "string",
    json: "Record<string, any>", media: "string", enum: "string",
    uuid: "string", email: "string", url: "string", password: "string",
  };
  return map[type] || "any";
}

function fieldTypeToPrisma(type: string): string {
  const map: Record<string, string> = {
    text: "String", number: "Float", boolean: "Boolean",
    date: "DateTime", timestamp: "DateTime", relation: "String",
    json: "Json", media: "String", enum: "String",
    uuid: "String", email: "String", url: "String", password: "String",
  };
  return map[type] || "String";
}
